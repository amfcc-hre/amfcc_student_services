create table if not exists public.gate_passes (
  id uuid primary key default gen_random_uuid(),
  student_id text not null references public.students(id) on delete restrict,
  destination text not null,
  reason text not null,
  departure_at timestamptz not null,
  expected_return_at timestamptz not null,
  contact_details text not null,
  status text not null default 'pending' check (status in ('pending','approved','rejected','cancelled','departed','returned','expired')),
  submitted_at timestamptz not null default now(),
  final_approved_at timestamptz,
  actual_departure_at timestamptz,
  actual_return_at timestamptz,
  paper_pass_checked boolean not null default false,
  cancelled_at timestamptz,
  cancelled_by_role text,
  cancellation_reason text,
  created_source text not null default 'student' check (created_source in ('student','staff','emergency_override')),
  updated_at timestamptz not null default now(),
  check (expected_return_at > departure_at)
);

create table if not exists public.gate_pass_approvals (
  id bigint generated by default as identity primary key,
  pass_id uuid not null references public.gate_passes(id) on delete cascade,
  approver_role text not null check (approver_role in ('administrator','principal','dean','director')),
  decision text not null check (decision in ('approved','rejected','cancelled')),
  comments text,
  decided_at timestamptz not null default now(),
  unique(pass_id, approver_role)
);

create table if not exists public.gate_pass_status_history (
  id bigint generated by default as identity primary key,
  pass_id uuid not null references public.gate_passes(id) on delete cascade,
  previous_status text,
  new_status text not null,
  actor_role text,
  notes text,
  changed_at timestamptz not null default now()
);

create table if not exists public.gate_duty_records (
  id bigint generated by default as identity primary key,
  student_id text not null references public.students(id) on delete restrict,
  direction text not null check (direction in ('IN','OUT')),
  scanned_at timestamptz not null default now(),
  gate_device_id uuid not null references public.gate_devices(id) on delete restrict,
  record_source text not null default 'scanner' check (record_source in ('scanner','manual'))
);

create table if not exists public.audit_log (
  id bigint generated by default as identity primary key,
  event_type text not null,
  entity_type text not null,
  entity_id text,
  actor_role text,
